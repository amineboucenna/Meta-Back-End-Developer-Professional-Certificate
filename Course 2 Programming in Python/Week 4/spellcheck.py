def word_count(sentence):
    words = len(sentence.split())
    print(words)
    return words

def char_count(sentence):
    chars = len(sentence)
    print(chars)
    return chars

def first_char(sentence):
    first = sentence[0]
    return first

def last_char(sentence):
    last = sentence[-1]
    return last
